numeroDia=int(input("Ingresa el numero del dia(1 a 7): "))
if numeroDia==1:
    print("Lunes")
elif numeroDia==2:
    print("Martes")
elif numeroDia==3:
    print("Miercoles")
elif numeroDia==4:
    print("Jueves")
elif numeroDia==5:
    print("Viernes")
elif numeroDia==6:
    print("Sabado")
elif numeroDia==7:
    print("Domingo")
